weather = input("What is the weather like today? \n")

if weather == "sunshine" or "sunny":
    print("You should go outside!")
elif weather == "rain" or "raining":
    print("You should stay inside!")
elif weather == "snow" or "snowing":
    print("You should go sledding!")
elif weather =="cloudy":
    print("You should go to the beach!")
elif weather == "windy":
    print("You should fly a kite!")
else:
    print("I don't know what to do!")
